#include "libft.h"

int		ft_negate(int color)
{
	int		r;
	int		g;
	int		b;

	r = 255 - (color >> 16);
	g = 255 - ((color >> 8) & 0xFF);
	b = 255 - (color & 0xFF);
	return (ft_getcolor(r, g, b));
}

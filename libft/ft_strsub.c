#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*sub;
	size_t	i;

	sub = ft_strnew(len);
	i = 0;
	if (sub)
	{
		while (len-- > 0)
			sub[i++] = s[start++];
	}
	return (sub);
}
